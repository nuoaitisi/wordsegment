package model;

public class Diff {
	private String id;
	private String compared_word;
	private String test_word;
	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
	public String getCompared_word() {
		return compared_word;
	}
	public void setCompared_word(String compared_word) {
		this.compared_word = compared_word;
	}
	public String getTest_word() {
		return test_word;
	}
	public void setTest_word(String test_word) {
		this.test_word = test_word;
	}
	
}
